#include <iostream>

int main()
{
	cout << "1 + 2 = " << 1 + 2 << endl;

	cout << "1 + 2 + 3 + 4 = " << 1 + 2 + 3 + 4 << endl;

	cout << "5 - 2 = " << 5 - 2 << endl;

	cout << "(-4) + (-2) = " << (-4) + (-2) << endl;
	
	cout << "4 - 3 + 2 - 1 - 5 * 10 = " << 4 - 3 + 2 - 1 - 5 * 10 << endl;

	cout << "4 * 2 + 3 * 5 = " << 4 * 2 + 3 * 5 << endl;

	cout << "10 / 5 + 6 / 1 - 72 / 9 = " << 10 / 5 + 6 / 1 - 72 / 9 << endl;

	cout << "10 % 2 * 5 + 16 % 13 * 5 / 2 = " << 10 % 2 * 5 + 16 % 13 * 5 / 2 << endl;

	cout << "10 + 2 - 5 * 6 / 2 % 4 = " << 10 + 2 - 5 * 6 / 2 % 4 << endl;
	
	cout << "5 * (2 + (-4)) - (7 / 2 + (4 * 2)) % 3 = "  << 5 * (2 + (-4)) - (7 / 2 + (4 * 2)) % 3 << endl;

	return 0;
}
